CREATE TABLE employees_details AS
SELECT
    EmployeeNumber,
    MonthlyIncome,
    JobSatisfaction,
    EnvironmentSatisfaction,
    PerformanceRating,
    TotalWorkingYears,
    YearsAtCompany,
    OverTime,
    PercentSalaryHike,
    RelationshipSatisfaction,
    WorkLifeBalance
FROM employees_hr;